package org.dcache.cdmi.filter;

import javax.ws.rs.core.Response;

import org.apache.cxf.jaxrs.ext.RequestHandler;
import org.apache.cxf.jaxrs.model.ClassResourceInfo;
import org.apache.cxf.message.Message;
import org.springframework.beans.factory.annotation.Required;

public class AuthorizationFilter implements RequestHandler
{
    private AuthorizationInterceptor interceptor;

    @Required
    public void setInterceptor(AuthorizationInterceptor interceptor)
    {
        this.interceptor = interceptor;
    }

    @Override
    public Response handleRequest(Message msg, ClassResourceInfo resourceClass)
    {
        interceptor.handleMessage(msg);
        return null;
    }

}
